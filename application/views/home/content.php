                            <h2 class="uk-text-bold">股东大手笔增持，这些业绩暴增股有看点</h2>
                            <p>不忘初心，方得始终——2017年投资总结</p>
                            <p>本来是不想写每年年底的投资总结的，因为近10多年来投资恒瑞医药获得成功并形成自己的一套选出一只好股票、长期且重仓持有的投资赚钱模式以后，我一直在寻找下一只恒瑞医药，而目前只看上了为数不多的几只模糊标的。再加上2017年稍微持有一些大蓝筹股就能轻轻松松录得50%以上的收益，而自己的收益和绝大多数的此类投资人相比根本不值一提，所以写总结的欲望不是很强烈，后来想了想，自己总结一下、和朋友交流一下还是有一点好处的，那就写一点吧。</p>
                            <p>我就不说2017年的收益了，也不说诸如“损失了金钱、收获了经验”之类的装13的话，仅仅谈一点2017年收获的感慨，知无不言言无不尽，仅此而已</p>
                            <p>我的答案还是和以前一样，那就是选择一只优秀的股票，重仓并长期持有，即“和优秀公司做朋友，和时间做朋友”。具体有3个步骤：选股、持股和正确的资金管理。选股最重要，持股次之，股谚“长线是金”说的就是在选择优秀股票的基础上持有足够长的时间。正确的资金管理是在投资正确的时候要敢于下重手，敢于重仓。而在股票走熊的时候重仓，那是南辕北辙的致命错误。</p>
